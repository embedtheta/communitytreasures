                <h1>House Music</h1>
                <p>House music is one of the most popular genres of electronic dance music. This innovative style originated in Chicago in the early 1980s. 
Early house music was generally dance-based music characterized by repetitive 4/4 beats, rhythms mainly provided by drum machines, off-beat hi-hat cymbals, and synthesized basslines.
The hypnotic song "On and On", produced in 1984 by Chicago DJ Jesse Saunders and co-written by Vince Lawrence, had the first elements that became staples of the early house sound.
The term "House music" originated from a Chicago club called The Warehouse, which existed from 1977 to 1983. Clubbers to The Warehouse were primarily black and Latino, who came to dance to music played by the club's resident DJ Frankie Knuckles, whom fans refer to as the "godfather of house".

